print(10+3)
print(10/3)
#if we add two dash bars the return will be a integer
print(10//3)

#the percent (%) sign in a equation returns the remainder of the division 
print(10%3)


#the use of ** means 10 to the power of 3
print(10**3)

#A way to increment a number
x = 10
x = x + 3
#another way to do the same thing is
y = 10
y += 3

print(x)
print(y)

#it also works with others operators (-, /, //, %)
z = 10
z -=3
print(z)